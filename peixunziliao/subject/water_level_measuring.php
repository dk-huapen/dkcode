<h3> 差压式液位计用的平衡容器结构及工作原理 </h3>
当被测容器敞口时，气相压力为大气压。差压计的负压室通大气即可，此时也可用压力计来测量液位;若容器是密闭的，则需将差压计的负压室与容器的气相相连接。
通常被测介质的密度是已知的，压差△P与液位高度H成正比，测出压差就知道被测液位高度。
