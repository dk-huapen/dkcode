<h2><center>2020年技术培训</center></h2>
<h3><center>热控专业技术培训方案</enter></h3>
	热控专业目前共有人员14人，其中我厂人员11人，华电保运人员3人，我厂11人中检修负责人5人，普通检修工3人，新员工3人，检修负责人在检修技能和设备维护方面，已能够针对设备故障和问题进行分析并提出解决方案，普通检修工在基本检修技能方面，已能够独立地完成设备日常维护检修工作。新员工由于参加工作年限短，需要经过系统培训后才能够独立完成所负责区域的检修工作。华电保运人员虽然工作经验丰富但对于我厂部分设备（CEMS、DCS、汽轮机系统、化水仪表）未经过专门的系统培训，熟悉程度不够，需要针对性加强设备系统的培训后才能更好的完成检修工作。</br>
目前热控班组设备区域划分：
<ul>
<li>汽轮机：钱军明（负责人）、谢连科、赵玏钰</li>
<li>锅炉：李峰（负责人）、刘鑫伟（华电）、耿岩军（华电）、李艳虎（华电）</li>
<li>外围：王梦辰（负责人）、张鑫（新员工）</li>
<li>DCS：王亚斌（负责人）、牛韶娟（新员工）、程欣（新员工）</li>
</ul>
<ol type="I">
<li>培训目的</li>
<ol type="1">
<li>对2019年技术培训的内容和效果进一步强化和总结，充分利用2019年培训基础资料使每一位热控班成员可以真正掌握培训内容并应用到实际工作中从而更好的完成检修维护工作；</li>
<li>通过系统的培训提高热控班所有成员的技术水平，使每一位班组成员都能独立完成好自己所负责设备的检修维护工作，热控班组能够出色的完成我厂所有热工设备的维护检修工作；</li>
<li>通过培训满足机组运行、设备维护的基本要求，工作班成员能够独立完成自己所负责设备的检修维护工作，并有重点的培训一批专业技术带头人最为技术骨干，解决实际生产过程中出现的一些技术难题，完成技术攻关任务；</li>
<li>加强班组建设，打造学习型班组，提升全员素质，更好的服务于现场。</li>
<li>针对新员工，指定专门检修负责人进行一对一重点培养，使新员工可以尽快成长，能够独立地完成设备日常维护检修工作；</li>
<li>针对普通检修工，进行深入的培训，能够针对设备故障和问题独立的进行分析并提出解决方案；</li>
</ol>
<li>培训组织结构</li>
<ol>
<li>热工班长负责班组培训工作的全面监督检查工作，具体包含培训课件的检查、培训记录的检查、培训考试的监督、培训过程中的执行情况检查；</li>
<li>班组技术员负责班组培训的具体工作，包含培训计划的落实、培训的组织开展、自习和补习情况的监督落实，考试的安排以及培训台账的建立，培训资料的收集整理等等具体事项。</li>
</ol>
<li>培训方式</li>
<li>培训从二个方向同时进行，每个方向分别针对不同方面进行培训：</li>
<ol>
<li>班组集中培训：</li>
<ol type="1">
<li>培训内容：</li>
针对2019年培训内容和方向（侧重理论），对2019年培训内容进行总结归纳后挑选出12个课题,通过对比培训、现场实操、案例分析讨论（缺陷/事故总结）强化培训效果；
<li>培训方式：</li>
<ol type="a">
<li>每月安排4次技术培训，其中一次理论总结讲解，一次现场实操，一次案例分析讨论，一次根据现场实际需求由班组技术员组织培训，暂定每周周三下午安排1小时进行技术培训，可根据班组工作安排进行调整，但必须保证培训数量和质量；</li>
<li>;skdjf次技术培训缺席人员应在回归班组后进行自习；</li>
<ol>
<li>培训要求：</li>
<ol type="a">
<li>培训由授课负责人组织，授课负责人于周一将培训大纲和培训材料发送至热控班长处进行审核，通过后于培训前1日将培训资料发至班组文件群由参加培训人员提前熟悉、学习；</li>
		<li>b.授课负责人为每组负责人，培训大纲由授课负责人进行编制，培训资料由授课负责人协调本组人员进行收集整理。</li>
</ol>
4、验收方式：
<ol type="a">
a.每月月底由热控技术员进行培训笔记检查，热控班长组织考试验收；
b.考试要求全员参加；
c.技术员每日利用班前、后会，对近期培训内容进行随机抽查，检查掌握情况。
5、考核细则：
a.无培训笔记或培训笔记潦草、应付的考核绩效20分；
b.考试成绩后两名分别考核绩效50-100分，前两名分别奖励绩效50-100分，全员在80分以上不做考核；
c.考核成绩在70分以下者及未参与考试者，需进行补考，考核当月所有培训绩效；
d.授课人课件准备不认真考核绩效10分/次，（不认真的情形包括：课件内容不充分，不能保证1小时的学习时间；课件内容存在应付了事，编排混乱的情况；课件内容深度不够，过于简单；未按时提交专工审核；审核后未按要求整改即开展培训），课件准备认真充分奖励绩效10分/次；
e.班组未按时开展培训、且未向部门说明情况安排补训时间，考核班组负责人绩效10分/次；
f.班组日常抽查回答错误的，考核绩效10分/次。
</ol>
<ol>
<li>班组人员自学</li>
<ol type="1">
<li>培训内容：</li>
<ol type="a">
班组统一下发热空专业相关规范，个人进行自学；
每周指定《DL/T 774-2015 火力发电厂热工自动化系统检修运行维护规程》学习内容，个人进行自学；
每周集体技术培训资料提前发至班组群内，在该次培训提前进行自学，培训前由班组技术员进行提问；
<ol>
<li>培训方式：</li>
采取自学的培训方式，根据培训计划，每周不少于2小时的自学；
<li>验收方式：</li>
<ol type="a">
a.班组技术员每日利用班前、后会，对近期培训内容进行随机抽查，检查掌握情况；
b.每月月底由班组技术员收集培训笔记，送至班长处进行检查；
<li>考核细则：</li>
a.无培训笔记或培训笔记潦草、应付的考核绩效10分/次；
b.随机抽查回答错误的，考核绩效5分/次。
